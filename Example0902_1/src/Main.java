
public class Main {

	public static void main(String[] args) {
		int firstNum = 10;
		int secondNum = 5;
		
		System.out.println("firstNum + secondNum = " + (firstNum + secondNum));
		System.out.println("firstNum - secondNum = " + (firstNum - secondNum));
		System.out.println("firstNum * secondNum = " + (firstNum * secondNum));
		System.out.println("firstNum / secondNum = " + (firstNum / secondNum));
		System.out.println("firstNum % secondNum = " + (firstNum % secondNum));
	}
}
