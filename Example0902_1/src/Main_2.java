
public class Main_2 {

	public static void main(String[] args) {
		int number1 = 184;
		int number2 = 91;
		int number3 = 8;
		int number4 = 37;
		int number5 = 5;
		
		System.out.println("184를 2로 나눈 나머지 : " + number1 % 2);
		System.out.println("91을 4로 나눈 나머지 : " + number2 % 4);
		System.out.println("8을 5로 나눈 나머지 : " + number3 % 5);
		System.out.println("37을 6으로 나눈 나머지 : " + number4 % 6);
		System.out.println("5를 7로 나눈 나머지 : " + number5 % 7);
		
	}
}
